#include "libft.h"

int	ft_arr_has_str(char **arr, char *str)
{
	int		i;

	i = 0;
	while (arr[i])
	{
		if (ft_strcmp(arr[i], str) == 0)
			return (1);
		i++;
	}
	return (0);
}
